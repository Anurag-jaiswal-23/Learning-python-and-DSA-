# type conversion - converting one data type to another data type

# implicit type conversion - python automatically converts one data type to another data type based on the value assigned to it.

# explicit type conversion - we can explicitly convert one data type to another data type using the built-in functions like int(), float(), str(), etc.

# type conversion does not change original value of the variable but creates a new variable with the converted value.

# type casting is permanent conversion of one data type to another data type. It changes the original value of the variable.