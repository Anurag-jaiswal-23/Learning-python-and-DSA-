# dynamic typing - python is a dynamically typed language which means that we don't need to declare the data type of a variable while declaring it. The data type of a variable is determined at runtime based on the value assigned to it.

# static typing - in static typing languages we need to declare the data type of a variable while declaring it. The data type of a variable is determined at compile time based on the value assigned to it.

# dynamic binding - in dynamic binding languages the data type of a variable can be changed at runtime based on the value assigned to it.

# static binding - in static binding languages the data type of a variable cannot be changed at runtime based on the value assigned to it.

# runtime - the time during which a program is running
# compile time - the time during which a program is being compiled 
# compiler - a program that converts source code into machine code
# interpreter - a program that executes source code line by line

